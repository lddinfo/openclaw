fi
if [ -f "\${BUNDLE_DIR}/.openclaw/workspace/SOUL.md" ]; then
  mkdir -p "\$HOME/.openclaw/workspace"
  cp "\${BUNDLE_DIR}/.openclaw/workspace/SOUL.md" "\$HOME/.openclaw/workspace/SOUL.md"
fi
if [ -f "\${BUNDLE_DIR}/.openclaw/.env" ] && prompt_overwrite_file "\$HOME/.openclaw/.env" "~/.openclaw/.env"; then
  BUNDLE_ENV_CONTENT="\$(<"\${BUNDLE_DIR}/.openclaw/.env")"
  BUNDLE_ENV_CONTENT="\${BUNDLE_ENV_CONTENT//__OPENCLAW_HOME__/\$HOME/.openclaw}"
  EXISTING_ENV_PATH="\$HOME/.openclaw/.env"
  TMP_ENV_PATH="\$HOME/.openclaw/.env.tmp.\$\$"
  printf '%s\n' "\${BUNDLE_ENV_CONTENT}" > "\${TMP_ENV_PATH}"
  python3 - "\${TMP_ENV_PATH}" "\${EXISTING_ENV_PATH}" <<'PY'
import pathlib
import re
import sys

bundle_path = pathlib.Path(sys.argv[1])
existing_path = pathlib.Path(sys.argv[2])
preserve_keys = [
    "AGENT_BOT_API_BASE_URL",
    "CONTROL_PLANE_BASE_URL",
    "AGENT_BOT_BASE_URL",
]

def parse_env(path):
    result = {}
    if not path.exists():
        return result
    text = path.read_text(encoding="utf-8")
    for key in preserve_keys:
        matches = re.findall(rf'^[ \t]*{re.escape(key)}[ \t]*=[ \t]*(.+)[ \t]*$', text, re.MULTILINE)
        if matches:
            result[key] = matches[-1].strip()
    return result

bundle_text = bundle_path.read_text(encoding="utf-8")
existing = parse_env(existing_path)
bundle = parse_env(bundle_path)

merged_lines = bundle_text.splitlines()
for key in preserve_keys:
    value = bundle.get(key) or existing.get(key)
    if value:
        merged_lines = [
            line for line in merged_lines
            if not re.match(rf'^[ \t]*{re.escape(key)}[ \t]*=', line)
        ]
        merged_lines.append(f"{key}={value}")

bundle_path.write_text("\n".join(merged_lines).rstrip("\n") + "\n", encoding="utf-8")
PY
  mv "\${TMP_ENV_PATH}" "\${EXISTING_ENV_PATH}"
fi

echo "Installed local OpenClaw package: \${PACKAGE_NAME}"
echo "Merged ~/.openclaw/openclaw.json:"
echo "  - preserved target machine agents.list"
echo "  - rewrote source-machine home paths to \$HOME"
echo "Next:"
echo "  Foreground run:"
echo "    export OPENCLAW_BRIDGE_TOKEN=<same token as control plane>"
echo "    export OPENCLAW_CONTROL_PLANE_STATE_FILE=\$HOME/.openclaw/control-plane-state.json"
echo "    openclaw gateway run --config ~/.openclaw/openclaw.json"
echo "  launchd service:"
echo "    # .env was restored to \$HOME/.openclaw/.env"
echo "    openclaw gateway install"
echo "    openclaw gateway status --deep"
EOF

chmod +x "${OUT_DIR}/install-local-openclaw.sh"

echo "Creating archive: ${OUT_TAR}"
tar -czf "${OUT_TAR}" -C "${ARTIFACTS_ROOT}" "${OUT_BASENAME}"

echo
echo "Done."
echo "Bundle path:"
echo "  ${OUT_TAR}"
echo
echo "Copy this archive to the target Mac, extract it, then follow README-import.md."
