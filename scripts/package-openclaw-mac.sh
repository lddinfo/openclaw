#!/usr/bin/env bash

set -euo pipefail

# Bundle the current local OpenClaw build together with a minimal runtime
# profile so the target Mac installs THIS repository version instead of
# upstream openclaw@latest.

OPENCLAW_HOME="${OPENCLAW_HOME:-$HOME/.openclaw}"
# Runtime JSON profile files bundled into the archive (override for your machine).
OPENCLAW_MAC_PROFILE_SOURCE="${OPENCLAW_MAC_PROFILE_SOURCE:-/Users/lidongdong/Desktop/openclaw}"
REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
ARTIFACTS_ROOT="${OPENCLAW_MAC_BUNDLE_OUTPUT_DIR:-${REPO_ROOT}/.artifacts/openclaw-mac-bundles}"
TIMESTAMP="$(date +%Y%m%d-%H%M%S)"
OUT_BASENAME="openclaw-mac-bundle-${TIMESTAMP}"
OUT_DIR="${ARTIFACTS_ROOT}/${OUT_BASENAME}"
OUT_TAR="${ARTIFACTS_ROOT}/${OUT_BASENAME}.tar.gz"
PACKAGE_DIR="${OUT_DIR}/packages"
LOCAL_PACKAGE_TGZ=""
LOCAL_PACKAGE_PATH=""
BUNDLE_ENV_PATH="${OUT_DIR}/.openclaw/.env"
SOURCE_HOME_FOR_REWRITE="${HOME}"
SOURCE_OPENCLAW_HOME="${OPENCLAW_HOME}"

echo "Packing OpenClaw runtime profile from: ${OPENCLAW_HOME}"
echo "Bundling config JSON from: ${OPENCLAW_MAC_PROFILE_SOURCE}"
echo "Using local repository: ${REPO_ROOT}"
echo "Writing bundle artifacts to: ${ARTIFACTS_ROOT}"

mkdir -p "${ARTIFACTS_ROOT}"
mkdir -p "${OUT_DIR}/.openclaw"
mkdir -p "${PACKAGE_DIR}"

copy_if_exists() {
  local src="$1"
  local dst="$2"
  if [ -f "${src}" ]; then
    mkdir -p "$(dirname "${dst}")"
    cp "${src}" "${dst}"
    echo "  [profile] ${src} -> ${dst}"
  else
    echo "  [profile] skipped missing file: ${src}"
  fi
}

# Match scripts/openclaw-prepack.ts: prepack skips rebuild when OPENCLAW_PREPACK_PREPARED is set
# only if these artifacts exist.
openclaw_dist_looks_ready() {
  local root="$1"
  if [ ! -f "${root}/dist/control-ui/index.html" ]; then
    return 1
  fi
  if [ ! -f "${root}/dist/index.js" ] && [ ! -f "${root}/dist/index.mjs" ]; then
    return 1
  fi
  shopt -s nullglob
  local assets=( "${root}/dist/control-ui/assets/"* )
  shopt -u nullglob
  local p
  for p in "${assets[@]}"; do
    if [ -f "${p}" ]; then
      return 0
    fi
  done
  return 1
}

dotenv_quote() {
  local value="$1"
  value="${value//\\/\\\\}"
  value="${value//\"/\\\"}"
  value="${value//$'\n'/\\n}"
  printf '"%s"' "${value}"
}

read_existing_bridge_token() {
  local source_env="$1"
  [ -f "${source_env}" ] || return 1

  python3 - "${source_env}" <<'PY'
import pathlib
import re
import sys

path = pathlib.Path(sys.argv[1])
text = path.read_text(encoding="utf-8")
matches = re.findall(r'^[ \t]*OPENCLAW_BRIDGE_TOKEN[ \t]*=[ \t]*(.+)[ \t]*$', text, re.MULTILINE)
if not matches:
    raise SystemExit(1)
value = matches[-1].strip()
if (value.startswith('"') and value.endswith('"')) or (value.startswith("'") and value.endswith("'")):
    value = value[1:-1]
print(value)
PY
}

read_existing_env_value() {
  local source_env="$1"
  local key="$2"
  [ -f "${source_env}" ] || return 1

  python3 - "${source_env}" "${key}" <<'PY'
import pathlib
import re
import sys

path = pathlib.Path(sys.argv[1])
key = sys.argv[2]
text = path.read_text(encoding="utf-8")
matches = re.findall(rf'^[ \t]*{re.escape(key)}[ \t]*=[ \t]*(.+)[ \t]*$', text, re.MULTILINE)
if not matches:
    raise SystemExit(1)
value = matches[-1].strip()
if (value.startswith('"') and value.endswith('"')) or (value.startswith("'") and value.endswith("'")):
    value = value[1:-1]
print(value)
PY
}

create_bundle_env() {
  local dst="$1"
  local source_env="${OPENCLAW_HOME}/.env"
  local generated_bridge_token="${OPENCLAW_BRIDGE_TOKEN:-}"
  local agent_bot_api_base_url="${AGENT_BOT_API_BASE_URL:-}"
  local control_plane_base_url="${CONTROL_PLANE_BASE_URL:-}"
  local agent_bot_base_url="${AGENT_BOT_BASE_URL:-}"

  if [ -z "${generated_bridge_token}" ]; then
    generated_bridge_token="$(read_existing_bridge_token "${source_env}" 2>/dev/null || true)"
  fi
  if [ -z "${generated_bridge_token}" ]; then
    generated_bridge_token="change-me"
  fi
  if [ -z "${agent_bot_api_base_url}" ]; then
    agent_bot_api_base_url="$(read_existing_env_value "${source_env}" "AGENT_BOT_API_BASE_URL" 2>/dev/null || true)"
  fi
  if [ -z "${control_plane_base_url}" ]; then
    control_plane_base_url="$(read_existing_env_value "${source_env}" "CONTROL_PLANE_BASE_URL" 2>/dev/null || true)"
  fi
  if [ -z "${agent_bot_base_url}" ]; then
    agent_bot_base_url="$(read_existing_env_value "${source_env}" "AGENT_BOT_BASE_URL" 2>/dev/null || true)"
  fi

  cat > "${dst}" <<EOF
# Generated by scripts/package-openclaw-mac.sh
OPENCLAW_BRIDGE_TOKEN=$(dotenv_quote "${generated_bridge_token}")
EOF
  echo "  [profile] generated minimal env: ${dst}"

  cat >> "${dst}" <<'EOF'
OPENCLAW_CONTROL_PLANE_STATE_FILE="__OPENCLAW_HOME__/control-plane-state.json"
EOF

  if [ -n "${agent_bot_api_base_url}" ]; then
    cat >> "${dst}" <<EOF
AGENT_BOT_API_BASE_URL=$(dotenv_quote "${agent_bot_api_base_url}")
EOF
  fi
  if [ -n "${control_plane_base_url}" ]; then
    cat >> "${dst}" <<EOF
CONTROL_PLANE_BASE_URL=$(dotenv_quote "${control_plane_base_url}")
EOF
  fi
  if [ -n "${agent_bot_base_url}" ]; then
    cat >> "${dst}" <<EOF
AGENT_BOT_BASE_URL=$(dotenv_quote "${agent_bot_base_url}")
EOF
  fi
}

echo
echo "Building and packing local OpenClaw package..."
pushd "${REPO_ROOT}" >/dev/null

# Fast path: if dist/ is already complete, prepack only verifies (no pnpm build / ui:build).
# Slow path: force full prepack with OPENCLAW_MAC_PACK_FORCE_PREPACK_BUILD=1
#   (or any incomplete dist — we run build here, then pack with skip so prepack does not rebuild twice).
force_prepack="${OPENCLAW_MAC_PACK_FORCE_PREPACK_BUILD:-}"
if [ "${force_prepack}" = "1" ] || [ "${force_prepack}" = "true" ]; then
  echo "  [pack] OPENCLAW_MAC_PACK_FORCE_PREPACK_BUILD: prepack will run full build + ui:build (slow)."
  unset OPENCLAW_PREPACK_PREPARED
elif openclaw_dist_looks_ready "${REPO_ROOT}"; then
  echo "  [pack] dist/ looks ready — OPENCLAW_PREPACK_PREPARED=1 (prepack verifies only, no rebuild)."
  export OPENCLAW_PREPACK_PREPARED=1
else
  echo "  [pack] dist/ missing or incomplete — running pnpm build && pnpm ui:build..."
  pnpm build
  pnpm ui:build
  echo "  [pack] OPENCLAW_PREPACK_PREPARED=1 for pack (prepack verifies only)."
  export OPENCLAW_PREPACK_PREPARED=1
fi

# Prepack is fast when OPENCLAW_PREPACK_PREPARED=1; pnpm pack still walks and gzip-compresses
# everything in package.json "files" (dist/, docs/, skills/, assets/, …). That step alone often
# takes several minutes on a large tree — not stuck, just I/O + compression.
echo "  [pack] Creating npm tarball (dist + docs + skills + …) — this can take a few minutes…"

LOCAL_PACKAGE_TGZ="$(pnpm pack --pack-destination "${PACKAGE_DIR}" | tail -n 1 | tr -d '\r')"
popd >/dev/null

if [ -z "${LOCAL_PACKAGE_TGZ}" ]; then
  echo "Failed to produce local OpenClaw tarball" >&2
  exit 1
fi

if [[ "${LOCAL_PACKAGE_TGZ}" = /* ]]; then
  LOCAL_PACKAGE_PATH="${LOCAL_PACKAGE_TGZ}"
  LOCAL_PACKAGE_TGZ="$(basename "${LOCAL_PACKAGE_PATH}")"
else
  LOCAL_PACKAGE_PATH="${PACKAGE_DIR}/${LOCAL_PACKAGE_TGZ}"
fi

if [ ! -f "${LOCAL_PACKAGE_PATH}" ]; then
  echo "Failed to produce local OpenClaw tarball" >&2
  exit 1
fi

echo "  [package] ${LOCAL_PACKAGE_PATH}"

copy_if_exists "${OPENCLAW_MAC_PROFILE_SOURCE}/openclaw.json" "${OUT_DIR}/.openclaw/openclaw.json"
copy_if_exists "${OPENCLAW_MAC_PROFILE_SOURCE}/exec-approvals.json" "${OUT_DIR}/.openclaw/exec-approvals.json"
copy_if_exists "${OPENCLAW_HOME}/workspace/SOUL.md" "${OUT_DIR}/.openclaw/workspace/SOUL.md"
create_bundle_env "${BUNDLE_ENV_PATH}"

cat > "${OUT_DIR}/README-import.md" <<EOF
